/**
 * @ORM\Entity(repositoryClass=MaquetteRepository::class)
 */
class Maquette
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    public function getId(): ?int
    {
        return $this->id;
    }
}
